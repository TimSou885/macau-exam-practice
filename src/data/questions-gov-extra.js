function opts(...lines) {
  return lines.map((t, i) => ({ key: String.fromCharCode(65 + i), text: t }))
}

function qShared(uid, question, options, correctKey, explanation) {
  return {
    id: `law-gov-extra-${uid}`,
    examType: 'both',
    category: 'law',
    question,
    options,
    correctKey,
    explanation,
  }
}

